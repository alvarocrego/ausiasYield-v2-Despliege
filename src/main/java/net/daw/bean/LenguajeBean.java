package net.daw.bean;

/**
 *
 * @author Alvaro
 */
public class LenguajeBean {

    private int id = 0;
    private String nombre = "";

    public LenguajeBean() {
        
    }

    public LenguajeBean(int id) {
        this.id = id;
    }
    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

}
